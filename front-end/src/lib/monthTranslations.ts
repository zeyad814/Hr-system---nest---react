export const getMonthName = (month: number, language: 'ar' | 'en'): string => {
  const monthKeys = [
    'january', 'february', 'march', 'april',
    'may', 'june', 'july', 'august',
    'september', 'october', 'november', 'december'
  ];

  const monthKey = monthKeys[month - 1];
  return monthTranslations[language][monthKey as keyof typeof monthTranslations.ar] || monthKey;
};
